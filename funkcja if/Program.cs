using System;

namespace funkcja_if
{
    class Program
    {
        static void Main()
        {
            bool pierwszaZmiennaTypuBoolean = false;
            bool drugaZmiennaTypuBoolean = false;
            bool trzeciaZmiennaTypuBoolean = false;

            if (pierwszaZmiennaTypuBoolean = true)
            {
                Console.WriteLine("Pierwsza zmienna typu Boolean jest warta true");
            }
            if (drugaZmiennaTypuBoolean = true)
            {
                Console.WriteLine("Druga zmienna typu Boolean jest warta true");
            }
            if (trzeciaZmiennaTypuBoolean = true)
            {
                Console.WriteLine("Trzecia zmienna typu Boolean jest warta true");
            }
            if (pierwszaZmiennaTypuBoolean || drugaZmiennaTypuBoolean) || trzeciaZmiennaTypuBoolean == false)
            {
                Console.WriteLine("Nie wiem jak, nie wiem gdzie, ale zepsułeś")
            }
        }

    }
}